/*
 comparison equality
 === and !== enforces both value and type equality
 == and != allows javascript to perform type coercion and only checks value equality
*/
var a = 1;
var b = '1';
if(a === b) {
    console.log('a equals b');
} else {
    console.log('a != b');
}

